class Variables
{
    public static void main(String a[])
    {
        byte A = 127;
        int B = 333;
        float C = 2.33f;
        double D = 1233.45;
        long E = 123l;
        char F = 'k';
        boolean G = true;
        short H = 2345;
        System.out.println("\n\n\nbyte A = "+A);
        System.out.println("int B = "+B);
        System.out.println("float C = "+C);
        System.out.println("double D = "+D);
        System.out.println("long E = "+E);
        System.out.println("char F = "+F);
        System.out.println("boolean G = "+G);
        System.out.println("short H = "+H+"\n\n\n");
    }
}